					<h2>Gréckokatolícky kostol</h2>
				</div>
				<p class="about container">
					V obci sa nachádza gréckokatolícky chrám (cerkev) zasvätený archanjelovi Michalovi, jednoloďový s presbytériom, postavený v rokoch 1913 – 1916, prestavaný v roku 1952 a rekonštruovaná v rokoch 1970 – 1980. Táto cerkev je filiálkou gréckokatolíckej farnosti Čirč. Nadpis pred vstupom do cerkvi vyrezávaný rezbárom pochádzajúcim z obce.

					Veľkou zaujímavosťou je sväté evanjelium, ktoré sa na prestole (oltári) v obci nachádza od roku 1899. Sväté evanjelium je však už z roku 1891. V tomto staroslovanskom evanjeliu je na spodnej časti strán evanjelia podľa Matúša od 1. až po 55. stranu vždy na každej druhej strane po jednom riadku napísaný záznam, ktorý bol takýmto spôsobom vpísaný pravdepodobne preto, aby nikto z evanjelia nevytrhol strany, lebo by musel odstrániť 55 strán. Tento záznam hovorí o tom, kto sväté evanjelium pre "Ruskovoľanskú" cerkev zakúpil.

					Cerkev archanjela Michala bola do roku 1913 drevená. V súčasnej podobe bola dokončená v roku 1916.
