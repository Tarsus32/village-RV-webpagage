					<p>
						Ruská Voľa nad Popradom je obec na Slovensku v okrese Stará Ľubovňa. Nachádza sa na severe Slovenska pri hraniciach s Poľskom, ktorú rozdeľuje Smrečný potok pri poľskej obci Leluchów. V obci sa do seba vlievajú dva potoky, a to Pálený potok, ktorý vyviera vo výške približne 885 m n. m. a Chotárny potok, ktorý vyviera vo výške približne 1 007 m n. m.. Pálený a chotárny potok sa vlieva do spomínaného Smrečného potoka a následne do rieky Poprad. Prvé písomné zmienky o obci pochádzajú z roku 1357. Obec leží na severozápadnom upätí Čergovského pohoria, v nadmorskej výške okolo 515 m n. m. Neďaleko obce preteká rieka Poprad, odtiaľ názov obce.
